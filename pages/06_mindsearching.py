import streamlit as st

# 성격 유형 선택
st.title('🔮 신비로운 융의 성격 유형 탐험! 🔮')

st.write("아니마는 남성의 안에 있는 여성적 자아를 뜻하고, 아니무스는 여성의 안에 있는 남성적 자아를 뜻합니다. 🌟 우주의 지혜를 빌려 당신 안에 있는 아니마와 아니무스를 탐구해보세요...")

# 성격 유형 선택
energy = st.selectbox('당신의 에너지는 어디로 향하나요? 🌌', ['외향성 (Extraversion)', '내향성 (Introversion)'])
function = st.selectbox('당신의 주된 기능은 무엇인가요? 🔮', ['사고 (Thinking)', '감정 (Feeling)', '감각 (Sensation)', '직관 (Intuition)'])

# 아니마 또는 아니무스와 소통하기 위한 추천
if st.button('🧙‍♂️ 내면의 지혜를 구하다 🧙‍♀️'):
    if energy == '외향성 (Extraversion)':
        if function == '사고 (Thinking)':
            st.write("✨ 당신은 세상을 분석하고, 이성적으로 접근하는 타입입니다. 하지만 가끔은 내면의 감정과도 소통해보세요. 💫 아니마는 당신의 꿈 속에서 더 깊은 감정의 세계를 열어줄 것입니다. 🌙")
        elif function == '감정 (Feeling)':
            st.write("💖 당신은 타인과의 관계 속에서 감정을 중시하는 사람입니다. 아니무스와의 소통을 통해 내면의 이성적 결정을 더욱 강화할 수 있습니다. 🛡️ 명상을 통해 그와 소통해보세요. 🧘‍♀️")
        elif function == '감각 (Sensation)':
            st.write("🌍 당신은 현실적인 세상에 깊이 뿌리내린 사람입니다. 아니마와의 만남을 통해 상상력을 키우고, 새로운 가능성을 탐색해보세요. 🌈 자연과의 교감을 통해 그녀와 연결될 수 있습니다. 🍃")
        elif function == '직관 (Intuition)':
            st.write("🔮 당신은 미래의 가능성에 집중하는 사람입니다. 하지만 가끔은 현실로 돌아와 내면의 감각과 연결해보세요. 🧙‍♂️ 아니무스는 당신의 발을 지상에 단단히 붙여줄 것입니다. 🌳")
    elif energy == '내향성 (Introversion)':
        if function == '사고 (Thinking)':
            st.write("🧠 당신은 깊은 사고의 세계를 탐구하는 사람입니다. 하지만 내면의 감정과도 연결해보세요. 아니마는 당신의 깊은 무의식 속에서 당신을 기다리고 있습니다. 🌕 예술을 통해 그녀와 대화해보세요. 🎨")
        elif function == '감정 (Feeling)':
            st.write("💗 당신은 내면의 감정에 충실한 사람입니다. 아니무스와의 소통을 통해 더 명확한 방향을 찾을 수 있을 것입니다. 📜 독서와 철학적 사유를 통해 그의 지혜를 얻으세요. 📚")
        elif function == '감각 (Sensation)':
            st.write("🌱 당신은 내면의 세계에 깊이 뿌리내린 사람입니다. 아니마와 연결하여 새로운 창조적 에너지를 끌어들일 수 있습니다. 🌺 음악과 춤을 통해 그녀와 교감하세요. 🎶")
        elif function == '직관 (Intuition)':
            st.write("🌠 당신은 깊은 내면의 직관에 의존하는 사람입니다. 아니무스와의 만남을 통해 당신의 직관을 현실에서 구현해보세요. 🔨 창작 활동을 통해 그의 힘을 빌려보세요. 🛠️")

st.write("🪄 **내면의 지혜는 언제나 당신과 함께 있습니다...** 🪄")
